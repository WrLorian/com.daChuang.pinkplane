package daChuang.pojo;

public class equit
{
    private Integer equitId;
    private String equitName;
    private Integer equitStatus;
    private Integer equitCelsius;

    public Integer getEquitId()
    {
        return equitId;
    }

    public void setEquitId(Integer equitId)
    {
        this.equitId = equitId;
    }

    public String getEquitName()
    {
        return equitName;
    }

    public void setEquitName(String equitName)
    {
        this.equitName = equitName;
    }

    public Integer getEquitStatus()
    {
        return equitStatus;
    }

    public void setEquitStatus(Integer equitStatus)
    {
        this.equitStatus = equitStatus;
    }

    public Integer getEquitCelsius()
    {
        return equitCelsius;
    }

    public void setEquitCelsius(Integer equitCelsius)
    {
        this.equitCelsius = equitCelsius;
    }
}
